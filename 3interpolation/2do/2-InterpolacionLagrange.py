''' 
Tarea : Interpolaci√≥n de Lagrange.
Dados (-2, 4),  (-1, -2),  (3, 5) y (4.3, -11) estimar el valor de y, cuando x = 7.7

'''
p1=(-2,4)
p3=(-1,-2)
p4=(3,5)
p5=(4.3,11)
x=7.7

def fn(x,p1,p3,p4,p5):
    a=((x-p3[0])/(p1[0]-p3[0]))
    b=((x-p4[0])/(p1[0]-p4[0]))
    c=((x-p5[0])/(p1[0]-p5[0]))
    d=a*b*c*p1[1]

    a1=((x-p1[0])/(p3[0]-p1[0]))
    b1=((x-p4[0])/(p3[0]-p4[0]))
    c1=((x-p5[0])/(p3[0]-p5[0]))
    d1=a1*b1*c1*p3[1]

    a2=((x-p1[0])/(p4[0]-p1[0]))
    b2=((x-p3[0])/(p4[0]-p3[0]))
    c2=((x-p5[0])/(p4[0]-p5[0]))
    d2=a2*b2*c2*p4[1]

    a3=((x-p1[0])/(p5[0]-p1[0]))
    b3=((x-p3[0])/(p5[0]-p3[0]))
    c3=((x-p4[0])/(p5[0]-p4[0]))
    d3=a3*b3*c3*p5[1]
      
    return d+d1+d2+d3

print(fn(x,p1,p3,p4,p5))
